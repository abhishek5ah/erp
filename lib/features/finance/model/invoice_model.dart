class Invoice {
  final String id;
  final String customer;
  final String date;
  final String dueDate;
  final String amount;
  final String status;
  final String email;
  final String phone;
  final String source;

  Invoice({
    required this.id,
    required this.customer,
    required this.date,
    required this.dueDate,
    required this.amount,
    required this.status,
    required this.email,
    required this.phone,
    required this.source,
  });
}





